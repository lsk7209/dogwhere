export async function generateMetadata({ params }: BlogPostPageProps): Promise<Metadata> {
  const { slug } = await params
  const post = blogPosts[slug]
  
  if (!post) {
    return {
      title: '블로그 포스트를 찾을 수 없습니다 | 어서오개',
    }
  }

  const keywords = post.seoKeywords?.join(', ') || post.tags.join(', ')
  const canonicalUrl = `https://eoseo-ogae.com/blog/${slug}`

  return {
    title: `${post.title} | 어서오개 블로그`,
    description: post.excerpt,
    keywords: keywords,
    authors: [{ name: post.author }],
    creator: post.author,
    publisher: '어서오개',
    formatDetection: {
      email: false,
      address: false,
      telephone: false,
    },
    metadataBase: new URL('https://eoseo-ogae.com'),
    alternates: {
      canonical: canonicalUrl,
    },
    openGraph: {
      title: post.title,
      description: post.excerpt,
      type: 'article',
      locale: 'ko_KR',
      siteName: '어서오개',
      publishedTime: post.date,
      modifiedTime: post.lastModified || post.date,
      authors: [post.author],
      section: post.category,
      tags: post.tags,
      images: [
        {
          url: post.image,
          width: 1200,
          height: 630,
          alt: post.title,
        },
      ],
      url: canonicalUrl,
    },
    twitter: {
      card: 'summary_large_image',
      title: post.title,
      description: post.excerpt,
      images: [post.image],
      creator: '@eoseo_ogae',
      site: '@eoseo_ogae',
    },
    robots: {
      index: true,
      follow: true,
      googleBot: {
        index: true,
        follow: true,
        'max-video-preview': -1,
        'max-image-preview': 'large',
        'max-snippet': -1,
      },
    },
    verification: {
      google: 'your-google-verification-code',
      yandex: 'your-yandex-verification-code',
      yahoo: 'your-yahoo-verification-code',
    },
    category: post.category,
    other: {
      'article:author': post.author,
      'article:published_time': post.date,
      'article:modified_time': post.lastModified || post.date,
      'article:section': post.category,
      'article:tag': post.tags.join(','),
    },
  }
}

